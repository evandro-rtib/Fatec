    CREATE TABLE TB_PESSOAS(
        ID NUMBER(10,0),
        NOME VARCHAR2(200) NOT NULL,
        DOC_CPF NUMBER(13,0),
        DOC_RG VARCHAR2(10),
        DATA_NASCIMENTO DATE,
        NUMERO_TELEFONE_1 NUMBER(10,0),
        NUMERO_TELEFONE_2 NUMBER(10,0),
        EMAIL VARCHAR2(150),
        ENDERECO VARCHAR2(100),
        NUMERO NUMBER(10,0),
        COMPLEMENTO VARCHAR2(50),
        CIDADE VARCHAR2(45),
        UF VARCHAR2(2),
        CEP NUMBER(8,0),
        SEXO CHAR( 1 ) CHECK (SEXO IN ( 'F',  'M' )),
        CONSTRAINT PK_PESSOA_ID PRIMARY KEY(ID)
    );
    
    CREATE TABLE TB_EMPRESAS(
        ID NUMBER (10,0),
        CNPJ NUMBER (15,0),
        RAZAO_SOCIAL VARCHAR2(200),
        NOME_FANTASIA VARCHAR2 (200),
        
        
        CONSTRAINT PK_EMPRESA_ID PRIMARY KEY (ID)
    );
    
    
    
    CREATE TABLE TB_UNIDADES(
        EMPRESA_ID NUMBER (10,0),
        ID NUMBER (10,0),
        UNIDADE NUMBER(5,0),
        BLOCO VARCHAR2 (10),
        QUADRA VARCHAR2 (10),
        LOTE VARCHAR2 (10),
        PROPRIETARIO_ID NUMBER(10,0),
        
        CONSTRAINT FK_UNIDADE_EMPRESA_ID FOREIGN KEY (EMPRESA_ID) REFERENCES TB_EMPRESAS,
        CONSTRAINT FK_UBIDADE_PROPRIETARIO_ID FOREIGN KEY (PROPRIETARIO_ID) REFERENCES TB_PESSOAS,
        CONSTRAINT PK_UNIDADE_ID PRIMARY KEY(ID)
        
    );
    
    CREATE TABLE TB_MORADORES(
        PESSOA_ID NUMBER(10,0),
        UNIDADE_ID NUMBER (10,0),
        PRIORIDADE NUMBER (2,0),
        PERMISSAO VARCHAR2(10),
        
        CONSTRAINT FK_PESSOA_MORADOR_ID FOREIGN KEY (PESSOA_ID) REFERENCES TB_PESSOAS,
        CONSTRAINT FK_UNIDADE_MORADOR_ID FOREIGN KEY (UNIDADE_ID) REFERENCES TB_UNIDADES (ID),
        CONSTRAINT PK_MORADOR_ID PRIMARY KEY(PESSOA_ID,UNIDADE_ID)
    );
    
    CREATE TABLE TB_VISITANTES(
        PESSOA_ID NUMBER(10,0),
        UNIDADE_ID NUMBER (10,0),
        
        CONSTRAINT FK_VISITANTE_PESSOA_ID FOREIGN KEY (PESSOA_ID) REFERENCES TB_PESSOAS,
        CONSTRAINT FK_VISITANTE_UNIDADE_ID FOREIGN KEY (UNIDADE_ID) REFERENCES TB_UNIDADES,
        CONSTRAINT PK_VISITANTE_ID PRIMARY KEY(PESSOA_ID,UNIDADE_ID)
    );
    
    CREATE TABLE TB_PRESTADORES(
        PESSOA_ID NUMBER(10,0),
        UNIDADE_ID NUMBER (10,0),
        NOME_EMPRESA VARCHAR2(100),
        
        CONSTRAINT FK_PERSTADOR_PESSOA_ID FOREIGN KEY (PESSOA_ID) REFERENCES TB_PESSOAS,
        CONSTRAINT FK_PRESTADOR_UNIDADE_ID FOREIGN KEY (UNIDADE_ID) REFERENCES TB_UNIDADES,
        CONSTRAINT PK_PRESTADOR_ID PRIMARY KEY(PESSOA_ID,UNIDADE_ID)
    );
